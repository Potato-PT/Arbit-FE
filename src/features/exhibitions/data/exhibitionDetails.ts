export function getExhibitionDetail(id: string | undefined) {
  return exhibitionDetails.find((item) => item.id === id)
}

export function addExhibitionReview(exhibitionId: string, input: ExhibitionReviewInput) {
  const exhibition = getExhibitionDetail(exhibitionId)

  if (!exhibition) {
    return undefined
  }

  const review: ExhibitionReview = {
    id: `review-${exhibitionId}-${Date.now()}`,
    rating: input.rating,
    content: input.content,
    author: '나',
    visitedAt: input.visitedAt,
    tone: 'warm',
    isPublic: input.isPublic,
  }

  exhibition.reviews = [review, ...exhibition.reviews]

  return review
}
